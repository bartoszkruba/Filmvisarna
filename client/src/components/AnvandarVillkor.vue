      <b>1.</b> Allmänt om medlemskapet
      Dessa villkor (”Medlemsvillkoren”) reglerar ditt medlemskap i Filmstaden:s kundklubb (”Filmstaden”).
      ”Medlemsvillkoren” utgör det avtal som gäller mellan dig (”Medlemmen”) och SF Bio AB (org nr 556035-1651) när du blivit medlem i Filmstaden, och du bör därför läsa Medlemsvillkoren noggrant. SF Bio AB förbehåller sig rätten att uppdatera Medlemsvillkoren från tid till annan. Om vi gör några väsentliga förändringar i Medlemsvillkoren meddelar vi dig via e-post.
      SF Bio AB förbehåller sig rätten att efter egen bedömning avsluta medlemskapet om detta missbrukas eller för det fall Medlemmen vid besök på SF Bio AB:s biografer agerar i strid med SF Bio AB:s vid var tid gällande ordningsregler. Ett avslutande av medlemskapet kan innebära att intjänade poäng förverkas. SF Bio AB förbehåller sig också rätten att avsluta kundklubben Filmstaden, men ska då informera dig om detta minst sex (6) månader i förväg via e-post och övriga medlemskanaler.
