      <b>2.</b> Personuppgiftshantering
      När du registrerar dig som Medlem i Filmstaden godkänner du att vi behandlar de personuppgifter du lämnar. I vår Personuppgiftspolicy finns information om hur vi behandlar dina personuppgifter, vad de används till och hur vi skyddar din integritet. Vi uppmanar dig att läsa igenom Personuppgiftspolicyn noggrant och att kontakta oss om du har några frågor. (Hur du kommer i kontakt med oss i frågor som rör vår personuppgifts-hantering framgår av Personuppgiftspolicyn.)
      <br>
      <br>

      <b>3.</b> Att bli medlem i SF Bio
      Medlemskap i Filmstaden skapas via registrering på www.filmstaden.se (”filmstaden.se”) eller via vår App ”Filmstaden” (”Appen”). Det är inte möjligt att registrera medlemskap i kassan på biograferna. Registrering görs sedan via e-post och du skapar din medlemsprofil och väljer ditt lösenord. Vi kommunicerar huvudsakligen med dig som medlem via våra digitala kanaler. Alla basfunktionerna för medlemskapet kommer du åt via ditt konto i Appen och på sf.se. Appen är också tänkt att vara en kanal för kommunikation i direkt anslutning till dina biobesök.
      När du registrerar dig som medlem får du frågan om du vill ta emot marknadsföring från oss, via e-mail, sms eller push-notiser. Du kan när som helst välja att sluta ta emot marknadsföring genom att ändra dina kontoinställningar i Appen eller på webben, eller genom att följa länken i respektive meddelande. Vi kommer även om du avsagt dig marknadsföringsmeddelanden att fortsätta att leverera dina biljetter och bekräftelser via e-post. Vi kommer också att fortsätta kommunicera kring ditt medlemskap.
      Det är bara fysiska personer som fyllt 16 år som kan vara medlemmar i Filmstaden.
      Ditt medlemskap är personligt och kan inte överlåtas eller användas av annan. Du ansvarar själv för att dina inloggningsuppgifter inte hamnar i andras händer. Om du tror att någon kan ha fått reda på dina inloggningsuppgifter, rekommenderar vi att du byter ditt lösenord omgående.
      Medlemskapet får endast användas för icke-kommersiellt bruk. Du får inte sälja poäng och du får inte använda medlemskapet på ett sådant sätt att SF Bio AB eller annan drabbas av olägenhet eller skada. Du får inte bryta mot svensk lag och du måste följa SF Bio AB:s regler.
      <br>
      <br>

      <b>4.</b> Medlemsprofil
      Du skapar din medlemsprofil genom att ange följande obligatoriska uppgifter; för- och efternamn, personnummer, din e-postadress och ditt mobilnummer. Om du inte har ett svenskt personnummer ber vi dig kontakta Kundservice.
      Medlemskapet är kortlöst och du identifierar dig som medlem med ditt personnummer eller med ditt digitala medlemskort som finns i Appen.
      På dina personliga sidor som du når via inloggning; ”Logga in” kan du se all information kring ditt medlemskap, och där kan du också hantera ditt medlemskap, se poäng och nivåer m.m. Du loggar in på Mina Sidor i Appen eller på sf.se med din mailadress.
      <br>
      <br>

      <b>5.</b> Medlemsår
      Ditt medlemskap i Filmstaden börjar det datum du blir medlem och gäller därefter i 12 månader (det första Medlemsåret). Medlemskapet förlängs därefter automatiskt i 12-månaders perioder, där varje 12-månaders period utgör ett Medlemsår. (Om du varit medlem i SF Bio AB:s tidigare lojalitetsprogram, Bioklubben, gäller särskilda regler, se punkt 12 nedan.)
      SF Bio AB förbehåller sig rätten att avsluta medlemskapet om inga besök eller köp i kiosken har gjorts under en period på tre år, varvid medlemskapet upphör, men självfallet kan du bli medlem på nytt vid ett senare tillfälle.
      Du kan närsomhelst säga upp ditt medlemskap i Filmstaden.
      <br>
      <br>

      <b>6.</b> Medlemsnivåer
      Medlemskapet har två nivåer; medlem och Guldmedlem.
      Guldmedlem blir den som under ett Medlemsår samlat in 1500 nivågrundande poäng* eller gjort 10 poängivande besök**.
      *) Poäng som inte är nivågrundanden är t.ex. välkomstpoäng, dvs sådana poäng som vi ger till nya medlemmar, och kampanjpoäng, t.ex. poäng från ”dubbla-poäng” kampanjer. Övriga poäng du tjänar är nivågrundande och påverkar din medlemsnivå.
      **) Besök som inte är poänggivande är t ex besök som du betalat med poäng eller fria besök, t.ex. förhandsvisningar, fribiljetter eller personalkort. Övriga besök är poänggivande och påverkar din medlemsnivå.
      Intjänade poäng/besök under ett Medlemsår (”Intjänandeåret”) ligger till grund för vilken medlemsnivå som ska gälla för dig det efterföljande Medlemsåret. Första gången du uppnår guldnivån gäller dock guldmedlemskapet också resterande del av Intjänandeåret, dvs du blir guldmedlem så snart du uppfyllt kraven i föregående stycke.
      <br>
      <br>

      <b>7.</b> Hur man tjänar poäng
      Du tjänar poäng på det du betalar för din egen biobiljett då du köper den i inloggat läge på Appen och på sf.se eller i våra kassor och på alla produkter du handlar i våra kiosker.
      Varje krona du betalar motsvarar 1 poäng.
      Poäng för biljettköp registreras på Mina sidor när den föreställning biljetten gäller för är slut.
      Poäng för inköp i våra kiosker registreras på Mina sidor direkt vid köptillfället. Observera att köp på restauranger och caféer som ligger inne i eller i anslutning till våra biografer, men som inte drivs av SF Bio AB, inte ger några poäng. Om du är tveksam, fråga innan du handlar om köpet är poänggrundande.
      Du kan också efterregistrera biljettköp upp till en vecka efter föreställningen. Detta gör du genom att kontakta Kundservice via sf.se.
      Men…
      …det finns som alltid vissa specialbestämmelser;
      Presentkort: Du får poäng på det du köper (din biljett och kioskinköp) när du använder presentkort som betalning, men du får inte poäng för köpet av själva presentkortet, dvs för att ladda kortet.
      Rabattkort: Köp av din biljett med rabattkort ger poäng. Köp av själva rabattkortet ger inte poäng.
      Reservationsavgifter: Om du köper din biljett efter att först ha reserverat den, tjänar du inte poäng på reservationsavgiften.
      Företagsbiljetter: När du använder en företagsbiljett som betalning för din
      biljett ger det alltid 50 poäng.
      Fribiljetter: Ger inga poäng
      Biljetter du köper för poäng: Ger inga poäng
      <br>
      <br>
      <b>8.</b> Fördela poäng till vänner
      Om du i inloggat läge på Appen eller filmstaden.se eller i våra kassor köper biljett också åt annan medlem i Filmstaden, kan du genom att lägga till dennes medlemsnummer i Filmstaden se till att denne får poäng för sin biljett.
      <br>
      <br>

      <b>9.</b> Handla för poäng
      Du kan i inloggat läge använda dina intjänade poäng direkt i Appen, på sf.se eller i våra kassor för att köpa biljetter (även till andra än dig själv) eller för att handla i vår kiosk. Poäng kan inte användas för biljettköp i våra biljettautomater. Du kan inte köpa biljett till IMAX eller till VIP-salongen på Filmstaden Bergakungen i Göteborg för poäng. För vissa föreställningar eller evenemang kan det förekomma begränsningar i rätten att använda poäng för att köpa biljetter.
      Vid köp av biljetter i Appen eller på filmstaden.se kopplas köpet direkt till ditt konto och du får tillgång till de biljetter du köpt som e-biljetter direkt i Appen. När du betalar med poäng i kassan på våra biografer krävs att du kan uppvisa giltig legitimation. Detta för att säkerställa att ingen obehörig använder dina poäng.
      När du betalar biljetter eller handlar i våra kiosker med poäng dras de direkt från kontot. Vid eventuellt återköp sätts poängen tillbaka på ditt konto om de fortfarande är giltiga.
      Hur många poäng som vid varje tid krävs för köp av biljett eller för produkter i våra kiosker, framgår på sf.se.
      Det är alltid de äldsta poängen som dras först när du handlar för poäng.
      <br>
      <br>

      <b>10.</b> Poängens giltighetstid
      Intjänade poäng är giltiga för köp det Medlemsår de tjänades in plus ytterligare två Medlemsår. Poäng som inte utnyttjas inom den perioden slutar att gälla. (Giltighetstiden för hur länge man kan handla för poängen har inget samband med bestämmandet av medlemsnivå, se punkt 6 ovan.)
      <br>
      <br>

